    void signinsystem(){
        String username = emailInput.getText();
        String password = passwordInput.getText();
        String path = "G://001. UIU//3rd Semester//Java_All_Project//PortfolioMakerApp2//src//Data";
        
        try{
            BufferedReader reader = new BufferedReader(new FileReader(path+"//login.txt"));
            String line;
                while ((line = reader.readLine()) != null) {
                    String[] parts = line.split(",");
                    if (username.equals(parts[0]) && password.equals(parts[1])) {
                        // Login successful
                        JOptionPane.showMessageDialog(this, "Login successfull");
                        dispose();
                        //new HomePage(username, parts[2]);
                        this.hide();
                        HomePage homepageObj = new HomePage();
                        homepageObj.show();
                        return;
                    }
                    else if(!username.equals(parts[0]) && !password.equals(parts[1])){
                        JOptionPane.showMessageDialog(this, "Email or password error");
                    }
                    
                }
        }catch(Exception e){
            
        }
    }
